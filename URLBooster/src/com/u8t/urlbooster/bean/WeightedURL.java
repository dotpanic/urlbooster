package com.u8t.urlbooster.bean;


public final class WeightedURL {
    
    private URLList.URL_ID id;
    private String url;
    private int urlWeight;
    private int objective;
    private String statTag;
    private int statistic;
    
    public WeightedURL(final URLList.URL_ID id, final String url, final int urlWeight, final int objective, final int statistic, final String statTag) {
        this.id = id;
        this.url = url;
        this.urlWeight = urlWeight;
        this.objective = objective;
        this.statistic = statistic;
        this.statTag = statTag;
    }
    
    public final String getURL() {
        return url;
    }
    
    public final void setURL(final String url) {
        this.url = url;
    }
    
    public final int getWeight() {
        return urlWeight;
    }
    
    public final void setWeight(final int urlWeight) {
        this.urlWeight = urlWeight;
    }

    public final int getObjective() {
        return objective;
    }

    public final void setObjective(final int objective) {
        this.objective = objective;
    }

    public final String getStatTag() {
        return statTag;
    }

    public final void setStatTag(final String statTag) {
        this.statTag = statTag;
    }

    public final URLList.URL_ID getId() {
        return id;
    }

    public final void setId(final URLList.URL_ID id) {
        this.id = id;
    }

    public final int getStatistic() {
        return statistic;
    }

    public final void setStatistic(final int statistic) {
        this.statistic = statistic;
    }
}
